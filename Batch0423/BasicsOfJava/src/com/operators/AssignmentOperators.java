package com.operators;

public class AssignmentOperators {
	public static void main(String[] args) {
		int no = 10;
		
		no = no + 5;
		// short hand operator
		no += 5;		// no = no + 5;
		no -= 5;		// no = no - 5;
		no *= 5;		// no = no * 5;
		no /= 5;		// no = no / 5;
		no %= 5;		// no = no % 5;
	}
}
